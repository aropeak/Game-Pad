//neighbors array
int neighbors[192][8];
//determines the tiles in each cardinal direction from the current tile
void initNeighbors(){
  for (int i = 0; i < tileW * tileH; i++){
    // General Rule
    int N = i - tileW; int NE = (i - tileW) + 1;  int E = i + 1;  int SE = (i + tileW) + 1;  
    int S = i + tileW; int SW = (i + tileW) - 1;  int W = i - 1;  int NW = (i - tileW) - 1;
    
    // Exceptions
    if(i < tileW){NW = -1; N = -1; NE = -1;}
    if(i > (tileW * tileH) - (tileW + 1)){SW = -1; S = -1; SE = -1;}
    if (i % tileW == 0){NW = -1; W = -1; SW = -1;}
    if ((i + 1) % tileW == 0){NE = -1; E = -1; SE = -1;}

    neighbors[i][0] = N;  neighbors[i][1] = NE;  neighbors[i][2] = E;  neighbors[i][3] = SE;  
    neighbors[i][4] = S;  neighbors[i][5] = SW;  neighbors[i][6] = W;  neighbors[i][7] = NW;  
  }
}

//boolean that determines if the player can move 
boolean canMove;

//checks to see if the player can move
boolean checkMove(int level, int curX, int curY, int curW, int curH) {
  //determines the x and y tile position of the player
  int curTileX = curX/20;
  int curTileY = curY/20;
  
  //determines the current tile of the player
  int curTile = curTileX + (curTileY * tileW);
  
  //assume the player can move and set the bool to true
  canMove = true;
   
  //runs through each of the neighbor tiles and runs tests to see if the player can move
  for(int i; i < 8; i++) {
    int whichTile = neighbors[curTile][i];               // Get Current Neighbor    
    int neighborX = (whichTile % tileW) * tileSize;      // Get X Value of Neighbor
    int neighborY = (whichTile / tileW) * tileSize;      // Get Y Value of Neighbor
    
    //5 test cases
    boolean isOut = false; if(interaction[level][whichTile] == 0xFF) {isOut = true;}
    boolean xMin = false; if(curX < neighborX + tileSize) {xMin = true;}
    boolean xMax = false; if(curX + curW > neighborX) {xMax = true;}
    boolean yMin = false; if(curY < neighborY + tileSize) {yMin = true;}
    boolean yMax = false; if(curY + curH > neighborY) {yMax = true;}
    
    //if all the test cases return true, then the player can not move
    if(isOut==true && xMin==true && xMax==true && yMin==true && yMax==true) {
      canMove = false;
    }
  }
  
  //return the value of the boolean
  return canMove;  
}
